nilai_a = int(input("Masukan Nilai A : "))
nilai_b = int(input("Masukan Nilai B : "))

print(f"hasil Nilai A ditambah Nilai B adalah  : {nilai_a + nilai_b}")
print(f"hasil Nilai A dikurangi Nilai B adalah : {nilai_a - nilai_b}")
print(f"hasil Nilai A dikali Nilai B adalah : {nilai_a * nilai_b}")